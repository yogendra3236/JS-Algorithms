// For a = [8, 3, 4, 5, 2, 8], the output should be
// arrayCenter(a) = [4, 5].
//
// Here min(a) = 2, avg(a) = 5.

function arrayCenter(a){
  var length = a.length;
  var arr = [];
  if (length % 2 == 0){
    arr.push(a[(length/2)-1], a[length/2]);
  }
  else {
    arr.push()
  }
}
arrayCenter([8, 3, 4, 5, 2, 8]);
